print('- Comparando dois números -')
num1 = int(input('Informe o primeiro número: '))
num2 = int(input('Informe o segundo número: '))
if num1 > num2:
    print(f'Maior: {num1}\nMenor: {num2}')
elif num2 > num1:
    print(f'Maior: {num2}\nMenor: {num1}')
else:
    print('Números iguais.')

    
